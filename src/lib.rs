use bitflags::bitflags;
use zerocopy::{BigEndian, FromBytes, Immutable, IntoBytes, KnownLayout, U16};

/// Size of the packet header in bytes. Must match the size of the C struct.
const HEADER_SIZE: u16 = 0x0c;

#[derive(Debug, FromBytes, IntoBytes, Immutable, KnownLayout)]
#[repr(C)]
pub struct PacketHeader {
    flag_size: U16<BigEndian>,
    session_id: U16<BigEndian>,
    ack_id: U16<BigEndian>,
    reserved: [u8; 4],
    sequence_id: U16<BigEndian>,
}

impl PacketHeader {
    pub fn new(
        flags: PacketFlag,
        session_id: u16,
        ack_id: u16,
        sequence_id: u16,
        payload_size: u16,
    ) -> Self {
        let flag_size = (payload_size + HEADER_SIZE) | ((flags.bits() as u16) << 11);

        Self {
            flag_size: U16::new(flag_size),
            session_id: U16::new(session_id),
            ack_id: U16::new(ack_id),
            reserved: [0; 4],
            sequence_id: U16::new(sequence_id),
        }
    }

    pub fn packet_size(&self) -> u16 {
        self.flag_size.get() & 0x07ff
    }

    pub fn payload_size(&self) -> u16 {
        self.packet_size() - (size_of_val(self) as u16)
    }

    pub fn flags(&self) -> Option<PacketFlag> {
        PacketFlag::from_bits(((self.flag_size.get() & 0xf800) >> 11) as u8)
    }

    pub fn session_id(&self) -> u16 {
        self.session_id.get()
    }

    pub fn ack_id(&self) -> u16 {
        self.ack_id.get()
    }

    pub fn sequence_id(&self) -> u16 {
        self.sequence_id.get()
    }
}

bitflags! {
    #[derive(Debug, Clone, Copy, PartialEq)]
    pub struct PacketFlag: u8 {
        const ACK_REQUEST = 0x01;
        const HELLO = 0x02;
        const RESEND = 0x04;
        const ACK = 0x10;
    }
}

#[derive(Debug, FromBytes, IntoBytes, Immutable, KnownLayout)]
#[repr(C)]
pub struct HelloData {
    sequence_id: u8,
    reserved: [u8; 7],
}

impl HelloData {
    pub fn new(sequence_id: u8) -> Self {
        Self {
            sequence_id,
            reserved: [0; 7],
        }
    }
}

impl Default for HelloData {
    fn default() -> Self {
        Self::new(1)
    }
}

#[derive(Debug, FromBytes, IntoBytes, Immutable, KnownLayout)]
#[repr(C)]
pub struct CommandHeader {
    size: U16<BigEndian>,
    reserved: [u8; 2],
    command: [u8; 4],
}

impl CommandHeader {
    pub fn size(&self) -> u16 {
        self.size.get()
    }
}

#[derive(Debug, FromBytes, IntoBytes, Immutable, KnownLayout)]
#[repr(C)]
pub struct VersionCommand {
    major: U16<BigEndian>,
    minor: U16<BigEndian>,
}

impl VersionCommand {
    pub fn major(&self) -> u16 {
        self.major.get()
    }
    pub fn minor(&self) -> u16 {
        self.minor.get()
    }
}

