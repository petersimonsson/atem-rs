#[derive(Debug, Clone, FromBytes, IntoBytes, Immutable, KnownLayout, Unaligned)]
#[repr(C)]
pub struct TimeData {
    hour: u8,
    minute: u8,
    second: u8,
    frame: u8,
    drop_frame: u8,
    reserved: [u8; 3],
}

impl TimeData {
    pub fn hour(&self) -> u8 {
        self.hour
    }

    pub fn minute(&self) -> u8 {
        self.minute
    }

    pub fn second(&self) -> u8 {
        self.second
    }

    pub fn frame(&self) -> u8 {
        self.frame
    }

    pub fn drop_frame(&self) -> bool {
        self.drop_frame != 0
    }
}

impl Display for TimeData {
    fn fmt(&self, f: &mut Formatter<'_>) -> std::fmt::Result {
        write!(
            f,
            "{:02}:{:02}:{:02}.{:02}",
            self.hour(),
            self.minute(),
            self.second(),
            self.frame()
        )
    }
}

