        let data: [u8; 0x1c] = [
            0x8, 0x1c, 0x80, 0x3, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x1, 0x0, 0x10, 0xea, 0x2a,
            0x53, 0x54, 0x6f, 0x44, 0x69, 0xe3, 0x18, 0xf3, 0xff, 0xff, 0xff, 0x88,
        ];

        let (header, remaining) = PacketHeader::ref_from_prefix(data.as_ref()).unwrap();

        println!("{:02X?} -> {:02X?}", header, remaining,);

        assert_eq!(header.packet_size(), 0x001c);
        assert_eq!(header.flags(), Some(PacketFlag::ACK_REQUEST));
        assert_eq!(header.session_id, 0x8003);
        assert_eq!(header.ack_id, 0x0000);
        assert_eq!(header.sequence_id, 0x0001);

