#[derive(Debug, Clone, FromBytes, IntoBytes, Immutable, KnownLayout, Unaligned)]
#[repr(C)]
pub struct CommandHeader {
    size: U16<BigEndian>,
    reserved: [u8; 2],
    command: [u8; 4],
}

impl CommandHeader {
    pub fn new(size: u16, command: [u8; 4]) -> Self {
        let mut ret = Self {
            size: U16::default(),
            reserved: [0; 2],
            command,
        };

        ret.size = U16::new(size + size_of_val(&ret) as u16);

        ret
    }

    pub fn size(&self) -> u16 {
        self.size.get()
    }

    pub fn data_size(&self) -> u16 {
        self.size() - size_of_val(self) as u16
    }

    pub fn command(&self) -> &[u8] {
        self.command.as_ref()
    }
}

impl Display for CommandHeader {
    fn fmt(&self, f: &mut Formatter<'_>) -> std::fmt::Result {
        write!(f, "{}", String::from_utf8_lossy(self.command.as_ref()))
    }
}

