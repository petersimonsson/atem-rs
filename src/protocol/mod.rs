    #[test]
    fn deserialize_hello_packet() {
        let data: [u8; 0x14] = [
            0x10, 0x14, 0x57, 0x06, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        ];

        let packet = Packet::<HelloData>::ref_from_bytes(data.as_ref()).unwrap();

        assert_eq!(packet.header.packet_size(), 0x0014);
        assert_eq!(packet.header.flags(), Some(PacketFlag::HELLO));
        assert_eq!(packet.header.session_id, 0x5706);
        assert_eq!(packet.header.ack_id, 0x0000);
        assert_eq!(packet.header.sequence_id, 0x0000);
        assert_eq!(packet.data.sequence_id, 0x01);
    }

