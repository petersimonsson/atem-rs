#[derive(Debug)]
pub enum Event {
    Time(TimeData),
    Topology(TopologyData),
    UnknownCommand(CommandHeader, Vec<u8>),
}

#[derive(Debug, thiserror::Error)]
pub enum EventError {
    #[error("Parse error: {0}")]
    ParseError(String, String),
}

pub type EventResult = std::result::Result<Event, EventError>;

pub fn parse_command(header: &CommandHeader, data: &[u8]) -> EventResult {
    match header.command() {
        b"Time" => {
            let time_command = TimeData::ref_from_bytes(data)
                .map_err(|e| EventError::ParseError("Time".to_string(), e.to_string()))?;
            Ok(Event::Time(time_command.to_owned()))
        }
        b"_top" => {
            let topology = TopologyData::ref_from_bytes(data)
                .map_err(|e| EventError::ParseError("Topology".to_string(), e.to_string()))?;
            Ok(Event::Topology(topology.to_owned()))
        }
        _ => {
            println!(
                "Unknown command: {} -> {:02X?}",
                String::from_utf8_lossy(header.command()),
                data
            );
            Ok(Event::UnknownCommand(header.clone(), data.to_vec()))
        }
    }
}

#[derive(Debug, FromBytes, IntoBytes, Immutable, KnownLayout)]
#[repr(C)]
pub struct Packet<D> {
    header: PacketHeader,
    data: D,
}

impl<D> Packet<D> {
    pub fn new(flags: PacketFlag, session_id: u16, ack_id: u16, sequence_id: u16, data: D) -> Self {
        let header = PacketHeader::new(
            flags,
            session_id,
            ack_id,
            sequence_id,
            size_of_val(&data) as u16,
        );

        Self { header, data }
    }

    pub fn header(&self) -> &PacketHeader {
        &self.header
    }

    pub fn data(&self) -> &D {
        &self.data
    }
}

#[derive(Debug, FromBytes, IntoBytes, Immutable, KnownLayout, Unaligned)]
#[repr(C)]
pub struct Command<D> {
    header: CommandHeader,
    data: D,
}

impl<D> Command<D> {
    pub fn new(command: [u8; 4], data: D) -> Self {
        let header = CommandHeader::new(size_of_val(&data) as u16, command);

        Self { header, data }
    }
}

#[derive(Debug, FromBytes, IntoBytes, Immutable, KnownLayout, Unaligned)]
#[repr(C)]
pub struct PacketHeader {
    flag_size: U16<BigEndian>,
    session_id: U16<BigEndian>,
    ack_id: U16<BigEndian>,
    reserved: [u8; 4],
    sequence_id: U16<BigEndian>,
}

impl PacketHeader {
    pub fn new(
        flags: PacketFlag,
        session_id: u16,
        ack_id: u16,
        sequence_id: u16,
        payload_size: u16,
    ) -> Self {
        let mut ret = Self {
            flag_size: U16::default(),
            session_id: U16::new(session_id),
            ack_id: U16::new(ack_id),
            reserved: [0; 4],
            sequence_id: U16::new(sequence_id),
        };

        let flag_size = (payload_size + size_of_val(&ret) as u16) | (flags.bits() as u16) << 11;
        ret.flag_size = U16::new(flag_size);

        ret
    }

    pub fn ack(session_id: u16, ack_id: u16, sequence_id: u16) -> Self {
        let mut ret = Self {
            flag_size: U16::default(),
            session_id: U16::new(session_id),
            ack_id: U16::new(ack_id),
            reserved: [0; 4],
            sequence_id: U16::new(sequence_id),
        };

        let flag_size = (size_of_val(&ret) as u16) | (PacketFlag::ACK.bits() as u16) << 11;
        ret.flag_size = U16::new(flag_size);

        ret
    }

    pub fn packet_size(&self) -> u16 {
        self.flag_size.get() & 0x07ff
    }

    pub fn payload_size(&self) -> u16 {
        self.packet_size() - (size_of_val(self) as u16)
    }

    pub fn flags(&self) -> Option<PacketFlag> {
        PacketFlag::from_bits(((self.flag_size.get() & 0xf800) >> 11) as u8)
    }

    pub fn session_id(&self) -> u16 {
        self.session_id.get()
    }

    pub fn ack_id(&self) -> u16 {
        self.ack_id.get()
    }

    pub fn sequence_id(&self) -> u16 {
        self.sequence_id.get()
    }
}

impl Display for PacketHeader {
    fn fmt(&self, f: &mut Formatter<'_>) -> std::fmt::Result {
        write!(
            f,
            "Flags: {:?}, Session ID: {:02X}, Ack ID: {:02X}, Sequence ID: {:02X}",
            self.flags(),
            self.session_id(),
            self.ack_id(),
            self.sequence_id()
        )
    }
}

bitflags! {
    #[derive(Debug, Clone, Copy, PartialEq)]
    pub struct PacketFlag: u8 {
        const ACK_REQUEST = 0x01;
        const HELLO = 0x02;
        const RESEND = 0x04;
        const ACK = 0x10;
    }
}

